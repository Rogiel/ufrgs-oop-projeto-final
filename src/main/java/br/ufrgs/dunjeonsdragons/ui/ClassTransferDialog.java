package br.ufrgs.dunjeonsdragons.ui;

import br.ufrgs.dunjeonsdragons.template.PlayerClassTemplate;

import javax.swing.*;
import java.awt.*;
import java.awt.event.*;
import java.util.List;

public class ClassTransferDialog extends JDialog {
    private JPanel contentPane;
    private JButton buttonOK;
    private JButton buttonCancel;
    private JComboBox comboBox1;

    private PlayerClassTemplate selectedPlayerClassTemplate;

    public ClassTransferDialog(List<PlayerClassTemplate> classTemplateList) {
        setContentPane(contentPane);
        setModal(true);
        getRootPane().setDefaultButton(buttonOK);

        buttonOK.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                onOK();
            }
        });

        buttonCancel.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                onCancel();
            }
        });

        PlayerClassTemplate[] array = new PlayerClassTemplate[classTemplateList.size()];
        classTemplateList.toArray(array);

        comboBox1.setModel(new DefaultComboBoxModel<>(array));
        comboBox1.setRenderer(new DefaultListCellRenderer() {
            @Override
            public Component getListCellRendererComponent(JList list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
                final PlayerClassTemplate classTemplate = (PlayerClassTemplate) value;
                JLabel renderer = (JLabel) super.getListCellRendererComponent(list, value, index,
                        isSelected, cellHasFocus);
                if (value != null) {
                    renderer.setText(classTemplate.getName());
                }
                return renderer;
            }
        });

        // call onCancel() when cross is clicked
        setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
        addWindowListener(new WindowAdapter() {
            public void windowClosing(WindowEvent e) {
                onCancel();
            }
        });

        // call onCancel() on ESCAPE
        contentPane.registerKeyboardAction(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                onCancel();
            }
        }, KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), JComponent.WHEN_ANCESTOR_OF_FOCUSED_COMPONENT);
    }

    private void onOK() {
        selectedPlayerClassTemplate = (PlayerClassTemplate) comboBox1.getSelectedItem();
        dispose();
    }

    private void onCancel() {
        selectedPlayerClassTemplate = null;
        dispose();
    }

    public PlayerClassTemplate getSelectedPlayerClassTemplate() {
        return selectedPlayerClassTemplate;
    }
}
